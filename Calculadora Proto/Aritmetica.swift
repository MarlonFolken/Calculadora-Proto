struct Aritmetica {
    func suma (num1: Float, num2: Float) -> Float {
        return num1 + num2
    }
    func resta (minuendo: Float, sustraendo: Float) -> Float {
        return minuendo - sustraendo
    }
    func multiplicacion (num1: Float, num2: Float) -> Float {
        return num1 * num2
    }
    func division (dividendo: Float, divisor: Float) -> Float {
        return dividendo
    }
}